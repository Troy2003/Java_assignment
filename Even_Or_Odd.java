/*
13. WAP to check if a given no is an even number or odd number.
*/

public class Even_Or_Odd {
    public static void main(String[] args) {
        int givenNumber = 8;

        if(givenNumber%2 == 0){
            System.out.println("Even Number");
        }
        else{
            System.out.println("Odd Number");
        }
    }
}
